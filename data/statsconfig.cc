    StatsConfig::StatsConfig(std::string name) {
        this->name = name;
    }
    StatsConfig::StatsConfig (
        std::string name,
        std::map<std::string, std::vector<std::string>> area,
        std::map<std::string, std::vector<std::string>> poi,
        std::map<std::string, std::vector<std::string>> line
    ) 
    {
        this->name = name;
        this->area = area;
        this->poi = poi;
        this->line = line;
    };

    int StatsConfigFile::read_yaml(std::string filename, std::vector<StatsConfig> &statsconfig) {
        yaml2::Yaml2 yaml;
        yaml.read(filename);
        for (auto it = std::begin(yaml.root.children); it != std::end(yaml.root.children); ++it) {
            std::map<std::string, std::vector<std::string>> area_tags;
            std::map<std::string, std::vector<std::string>> poi_tags;
            std::map<std::string, std::vector<std::string>> line_tags;
            for (auto type_it = std::begin(it->children); type_it != std::end(it->children); ++type_it) {
                for (auto value_it = std::begin(type_it->children); value_it != std::end(type_it->children); ++value_it) {
                    for (auto tag_it = std::begin(value_it->children); tag_it != std::end(value_it->children); ++tag_it) {
                        if (type_it->value == "area") {
                            area_tags[value_it->value].push_back(tag_it->value);
                        } else if (type_it->value == "poi") {
                            poi_tags[value_it->value].push_back(tag_it->value);
                        } else if (type_it->value == "line") {
                            line_tags[value_it->value].push_back(tag_it->value);
                        }
                    }
                }            
            }
            statsconfig.push_back(StatsConfig(it->value, area_tags, poi_tags, line_tags));
        }
        return 1;
    }

